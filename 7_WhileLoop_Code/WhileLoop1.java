




import java.util.*;
class WhileLoop1{


        public static void main(String [] args){

                Scanner sc = new Scanner(System.in);

		System.out.println("Enter the Range To Print Print 3 digit Number");
		int n = sc.nextInt();

		int i = 0;
		while(i<=n){

			System.out.print(100+i+",");

			i++;
		}
		System.out.println();



	}
}
